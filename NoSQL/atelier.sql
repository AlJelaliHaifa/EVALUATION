-- Que signifie le terme NoSQL ?
-- NoSQL signifie not only SQL.

-- Pourquoi parle-t-on de "non relationnel" ?
/*Un base de donnée relationnelle relie les informations entre elles dans des bases de données et les stock dans des tables (d'où le "relationnelle"), 
contrairement à celle-ci la base de donnée non relationelle les stock au format clé-valeur (ce qui signifie que la clé est une constate definissant un ensemble de données 
(nom de produit,couleur,image , libelle...) et de valeur qui appartient à cet ensemble et est variable ) ce qui optimise le stockage.*/

-- Comment sont nées les bases NoSQL ?
/*Suite à l'augmentation considérable du volume des bases de donnée, rendant les données difficiles à traiter et au Big Data(analyse de grands volumes de données) mettant à l'épreuve
les moteurs de bases de données traditionnels sont nées les bases NoSQL afin de repondre à ces problèmes.*/

-- Y-a-t-il un seul type de base NoSQL ?
/*A ma connaissances il existe quatres type de base NoSQL:
-Bases de données orientées par colonne
-Bases de données Clé-Valeur (Key Value)
-Bases de données graphiques
-Bases de données documentaires*/

